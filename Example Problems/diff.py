def diff21(n):  
    '''
    To define the function 'diff21' with one agrument 'n'
    '''
    if n >= 21:  # if n is greater than 21 or less than 21
        return (n - 21) * 2  # To return double if n is greater than 21
    else:
        return 21 - n  # To return if n is least than 21

print(diff21(10))  # To print this to caal the function 'diff21'


